package assignment;

public class data_types {

	public static void main(String[] args)
	{	
		byte a=2;
		short b=4;
		int c=16;
		long d=122333;
		double e=2.212;
		float f=12.2f;
		char g='Y';
		String h="Hello";
		boolean i=true;
		
		System.out.println("byte data :" +a);
		System.out.println("short data :" +b);
		System.out.println("int data :" +c);
		System.out.println("long data :" +d);
		System.out.println("double data :" +e);
		System.out.println("float data :" +f);
		System.out.println("char data :" +g);
		System.out.println("string data :" +h);
		System.out.println("boolean data :" +i);
				
	}
}

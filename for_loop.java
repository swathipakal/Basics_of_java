package assignment;

public class for_loop 

{
	public static void main(String[] args) 
	{
		System.out.println("Display from 10 to 40"); 
		   
	
     for(int i=10;i<=40;i++)
     {
	   System.out.println(i); 
	   
     }
	
     System.out.println("Display from -10 to -40"); 
	   
     for(int i=-10;i>=-40;i--)
     {
	   System.out.println(i); 
	   
     }
     
     System.out.println("Display from -10 to 10" ); 
	   
     
     for(int i=-10;i<=10;i++)
     {
	   System.out.println(i); 
	   
     }
	
   }
}